def harfNot(dersNot):
    if dersNot >= 80:
        print("A")
    elif dersNot >= 70:
        print("B")
    elif dersNot >= 60:
        print("C")
    elif dersNot >= 40:
        print("D")
    elif dersNot >= 10:
        print("F")
    else:
        print("FF")

harfNot(4)
harfNot(34)
harfNot(44)
harfNot(64)
harfNot(74)
harfNot(84)
        
